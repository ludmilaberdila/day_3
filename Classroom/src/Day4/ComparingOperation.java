public class ComparingOperation {
    public static void main(String[] args) {


        int a = 20, b = 15;
        //<,>,
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(b>=a);
        System.out.println(a=b);


    }
}